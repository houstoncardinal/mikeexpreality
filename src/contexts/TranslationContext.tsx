interface TranslationContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string;
  direction: "ltr" | "rtl";
  languages: LanguageOption[];
}

const TranslationContext = createContext<TranslationContextType | undefined>(undefined);

export function TranslationProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    const saved = localStorage.getItem("preferred-language");
    return (saved as Language) || "en";
  });

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem("preferred-language", lang);
    
    // Update document direction for RTL languages
    const langOption = languages.find(l => l.code === lang);
    document.documentElement.dir = langOption?.direction || "ltr";
    document.documentElement.lang = lang;
  };

  useEffect(() => {
    const langOption = languages.find(l => l.code === language);
    document.documentElement.dir = langOption?.direction || "ltr";
    document.documentElement.lang = language;
  }, [language]);

  const t = (key: string): string => {
    return translations[language]?.[key] || translations.en[key] || key;
  };

  const direction = languages.find(l => l.code === language)?.direction || "ltr";

  return (
    <TranslationContext.Provider value={{ language, setLanguage, t, direction, languages }}>
      {children}
    </TranslationContext.Provider>
  );
}

export function useTranslation() {
  const context = useContext(TranslationContext);
  if (!context) {
    throw new Error("useTranslation must be used within a TranslationProvider");
  }
  return context;
}
